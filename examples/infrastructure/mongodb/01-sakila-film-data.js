const database = db.getSiblingDB("myDatabase");

database.languages.drop();
database.films.drop();

database.createCollection("languages", {
  validator: {
    $jsonSchema: {
      bsonType: "object",
      required: ["languageId", "name", "lastUpdate"],
      properties: {
        languageId: { bsonType: "int" },
        name: { bsonType: "string" },
        lastUpdate: { bsonType: "date" },
      },
    },
  },
});

database.createCollection("films", {
  validator: {
    $jsonSchema: {
      bsonType: "object",
      required: [
        "filmId",
        "title",
        "description",
        "releaseYear",
        "languageId",
        "language",
        "rentalDuration",
        "rentalRate",
        "length",
        "replacementCost",
        "rating",
        "specialFeatures",
        "lastUpdate",
      ],
      properties: {
        filmId: { bsonType: "int" },
        title: { bsonType: "string" },
        description: { bsonType: "string" },
        releaseYear: { bsonType: "int" },
        languageId: { bsonType: "int" },
        language: { bsonType: "string" },
        rentalDuration: { bsonType: "int" },
        rentalRate: { bsonType: "double" },
        length: { bsonType: "int" },
        replacementCost: { bsonType: "double" },
        rating: { enum: ["G", "PG", "PG-13", "R", "NC-17"] },
        specialFeatures: {
          bsonType: "array",
          items: { bsonType: "string" },
        },
        lastUpdate: { bsonType: "date" },
      },
    },
  },
});

